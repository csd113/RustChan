// media/ffmpeg.rs
//
// FFmpeg binary detection and subprocess execution helpers.
//
// Design notes:
//   • Detection is done by running `ffmpeg -version` and checking the exit code.
//   • All subprocess invocations use `std::process::Command` with explicit
//     argument arrays — never shell strings — to eliminate injection surfaces.
//   • This module is called from synchronous contexts (spawn_blocking), so
//     std::process::Command is used instead of tokio::process::Command.
//   • Temp files passed to ffmpeg are created by the caller; this module only
//     executes the subprocess and reports success or failure.
//   • If ffmpeg exits non-zero, the error includes the trimmed stderr so
//     operators can diagnose codec or format issues without reading raw logs.

use anyhow::{Context, Result};
use std::path::Path;
use std::sync::LazyLock;
use std::thread;
use std::time::{Duration, Instant};
use std::process::{Command, Stdio};

fn ffmpeg_command() -> Command {
    Command::new(&crate::config::CONFIG.ffmpeg_path)
}

fn ffprobe_command() -> Command {
    Command::new(&crate::config::CONFIG.ffprobe_path)
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum StreamKind {
    AudioOnly,
    Video,
}

static ENCODER_LIST: LazyLock<Option<String>> = LazyLock::new(|| {
    ffmpeg_command()
        .args(["-encoders"])
        .stdout(Stdio::piped())
        .stderr(Stdio::null())
        .output()
        .ok()
        .map(|output| String::from_utf8_lossy(&output.stdout).into_owned())
});

/// Probe whether the `ffmpeg` binary is reachable on the current PATH.
///
/// Runs `ffmpeg -version` and returns `true` if the process exits successfully.
/// This is a synchronous, blocking call and is intended to be invoked at
/// startup (or lazily on first upload) inside a `spawn_blocking` task.
///
/// A `false` return means conversion and video-thumbnail features will be
/// unavailable; all callers must degrade gracefully.
#[must_use]
pub fn detect_ffmpeg() -> bool {
    ffmpeg_command()
        .arg("-version")
        .stdout(Stdio::null())
        .stderr(Stdio::null())
        .status()
        .map(|s| s.success())
        .unwrap_or(false)
}

/// Execute `ffmpeg` with the given argument slice.
///
/// All arguments must be pre-constructed, static strings — no user-supplied
/// data may appear in `args` directly.  File paths passed to ffmpeg must be
/// UUID-based names generated by the server.
///
/// Returns `Ok(())` on exit code 0, or an `Err` containing the trimmed
/// stderr output on any non-zero exit.
///
/// # Errors
/// Returns an error if ffmpeg cannot be spawned (binary missing, permission
/// denied) or if the process exits with a non-zero status code.
pub fn run_ffmpeg(args: &[&str]) -> Result<()> {
    let output = run_command_with_timeout(
        ffmpeg_command().args(args),
        &crate::config::CONFIG.ffmpeg_path,
        "ffmpeg",
    )?;

    if output.status.success() {
        Ok(())
    } else {
        Err(anyhow::anyhow!(
            "ffmpeg exited with {}: {}",
            output.status,
            String::from_utf8_lossy(&output.stderr).trim()
        ))
    }
}

/// Convert an image file to WebP using ffmpeg.
///
/// Uses quality 85 and strips all metadata (`-map_metadata -1`).
/// Input can be any format ffmpeg understands (JPEG, PNG, BMP, TIFF, GIF, …).
///
/// The `-loop 0` flag causes multi-frame inputs (GIF) to produce an animated
/// WebP that loops forever, matching the default GIF behaviour.  For
/// single-frame inputs ffmpeg silently ignores the flag.
///
/// # Errors
/// Returns an error if ffmpeg exits non-zero or cannot be spawned.
pub fn ffmpeg_image_to_webp(input: &Path, output: &Path) -> Result<()> {
    let in_str = path_to_str(input)?;
    let out_str = path_to_str(output)?;

    run_ffmpeg(&[
        "-loglevel",
        "error",
        "-i",
        in_str,
        "-c:v",
        "libwebp",
        "-quality",
        "85",
        "-loop",
        "0",
        "-map_metadata",
        "-1",
        "-y",
        out_str,
    ])
    .with_context(|| format!("image→webp conversion failed for {in_str}"))
}

/// Generate a WebP thumbnail from an image or video by extracting the first
/// frame and scaling to fit within `max_dim × max_dim`.
///
/// The `-2` height modifier ensures the scaled height is rounded to an even
/// number, required by many video codecs.  Aspect ratio is always preserved.
/// Thumbnail quality is fixed at 80 per project spec.
///
/// Works for both static images and video/animation sources.
///
/// # Errors
/// Returns an error if ffmpeg exits non-zero or cannot be spawned.
pub fn ffmpeg_thumbnail(input: &Path, output: &Path, max_dim: u32) -> Result<()> {
    let in_str = path_to_str(input)?;
    let out_str = path_to_str(output)?;
    let scale = format!("scale='if(gt(iw,ih),{max_dim},-2)':'if(gt(iw,ih),-2,{max_dim})'");

    run_ffmpeg(&[
        "-loglevel",
        "error",
        "-i",
        in_str,
        "-vframes",
        "1",
        "-vf",
        &scale,
        "-c:v",
        "libwebp",
        "-quality",
        "80",
        "-y",
        out_str,
    ])
    .with_context(|| format!("thumbnail generation failed for {in_str}"))
}

/// Probe whether a WebM container is audio-only or contains video streams.
///
/// # Errors
/// Returns an error if `ffprobe` cannot be spawned, times out, exits non-zero,
/// or reports no audio/video streams.
pub fn probe_stream_kind(path: &Path) -> Result<StreamKind> {
    let path_str = path_to_str(path)?;
    let output = run_command_with_timeout(
        ffprobe_command().args([
            "-v",
            "quiet",
            "-show_entries",
            "stream=codec_type",
            "-of",
            "default=noprint_wrappers=1:nokey=1",
            path_str,
        ]),
        &crate::config::CONFIG.ffprobe_path,
        "ffprobe",
    )?;

    if !output.status.success() {
        return Err(anyhow::anyhow!(
            "ffprobe exited with {}: {}",
            output.status,
            String::from_utf8_lossy(&output.stderr).trim()
        ));
    }

    let mut saw_audio = false;
    for line in String::from_utf8_lossy(&output.stdout).lines() {
        match line.trim() {
            "video" => return Ok(StreamKind::Video),
            "audio" => saw_audio = true,
            _ => {}
        }
    }

    if saw_audio {
        Ok(StreamKind::AudioOnly)
    } else {
        Err(anyhow::anyhow!(
            "ffprobe returned no audio or video streams for: {path_str}"
        ))
    }
}

/// Probe the primary video codec of a media file using `ffprobe`.
///
/// Returns the lowercase codec name (e.g. `"vp9"`, `"av1"`, `"h264"`) on
/// success.  `ffprobe` must be on the same PATH as `ffmpeg`.
///
/// # Errors
/// Returns an error if `ffprobe` cannot be spawned, exits non-zero, or its
/// output contains no recognisable codec name.
pub fn probe_video_codec(path: &str) -> Result<String> {
    let output = run_command_with_timeout(
        ffprobe_command().args([
            "-v",
            "quiet",
            "-select_streams",
            "v:0",
            "-show_entries",
            "stream=codec_name",
            "-of",
            "default=noprint_wrappers=1:nokey=1",
            path,
        ]),
        &crate::config::CONFIG.ffprobe_path,
        "ffprobe",
    )?;

    if !output.status.success() {
        return Err(anyhow::anyhow!(
            "ffprobe exited with {}: {}",
            output.status,
            String::from_utf8_lossy(&output.stderr).trim()
        ));
    }

    let codec = String::from_utf8_lossy(&output.stdout)
        .trim()
        .to_ascii_lowercase();

    if codec.is_empty() {
        return Err(anyhow::anyhow!(
            "ffprobe returned no codec name for: {path}"
        ));
    }

    Ok(codec)
}
// ─── Internal helpers ──────────────────────────────────────────────────────────

/// Convert a `Path` to a UTF-8 `&str`, returning a descriptive error if the
/// path contains non-UTF-8 bytes (rare on all supported platforms).
fn path_to_str(p: &Path) -> Result<&str> {
    p.to_str()
        .ok_or_else(|| anyhow::anyhow!("path contains non-UTF-8 characters: {}", p.display()))
}

fn run_command_with_timeout(
    command: &mut Command,
    program: &str,
    label: &str,
) -> Result<std::process::Output> {
    let timeout = Duration::from_secs(crate::config::CONFIG.ffmpeg_timeout_secs);
    let mut child = command
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .with_context(|| {
            format!("failed to spawn {label} binary '{program}' — is it installed and executable?")
        })?;
    let started = Instant::now();

    loop {
        if child.try_wait()?.is_some() {
            return child
                .wait_with_output()
                .with_context(|| format!("{label} I/O error"));
        }
        if started.elapsed() >= timeout {
            let _ = child.kill();
            let _ = child.wait();
            return Err(anyhow::anyhow!(
                "{label} timed out after {}s",
                timeout.as_secs()
            ));
        }
        thread::sleep(Duration::from_millis(50));
    }
}

/// Probe whether the current ffmpeg binary has the `libwebp` encoder compiled in.
///
/// Runs `ffmpeg -encoders` and scans stdout for a line containing `libwebp`.
/// Returns `true` only when ffmpeg is present AND the encoder is available.
///
/// This is a synchronous, blocking call intended for use at server startup
/// inside a `spawn_blocking` task, or called directly from the startup path
/// where blocking is acceptable.
#[must_use]
pub fn check_webp_encoder() -> bool {
    ENCODER_LIST
        .as_deref()
        .is_some_and(|stdout| stdout.lines().any(|line| line.contains("libwebp")))
}

