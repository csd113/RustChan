// media/ffmpeg.rs
//
// FFmpeg binary detection and subprocess execution helpers.
//
// Design notes:
//   • Detection is done by running `ffmpeg -version` and checking the exit code.
//   • All subprocess invocations use `std::process::Command` with explicit
//     argument arrays — never shell strings — to eliminate injection surfaces.
//   • This module is called from synchronous contexts (spawn_blocking), so
//     std::process::Command is used instead of tokio::process::Command.
//   • Temp files passed to ffmpeg are created by the caller; this module only
//     executes the subprocess and reports success or failure.
//   • If ffmpeg exits non-zero, the error includes the trimmed stderr so
//     operators can diagnose codec or format issues without reading raw logs.

use anyhow::{Context, Result};
use std::path::Path;
use std::process::{Command, Stdio};

/// Probe whether the `ffmpeg` binary is reachable on the current PATH.
///
/// Runs `ffmpeg -version` and returns `true` if the process exits successfully.
/// This is a synchronous, blocking call and is intended to be invoked at
/// startup (or lazily on first upload) inside a `spawn_blocking` task.
///
/// A `false` return means conversion and video-thumbnail features will be
/// unavailable; all callers must degrade gracefully.
#[must_use]
pub fn detect_ffmpeg() -> bool {
    Command::new("ffmpeg")
        .arg("-version")
        .stdout(Stdio::null())
        .stderr(Stdio::null())
        .status()
        .map(|s| s.success())
        .unwrap_or(false)
}

/// Execute `ffmpeg` with the given argument slice.
///
/// All arguments must be pre-constructed, static strings — no user-supplied
/// data may appear in `args` directly.  File paths passed to ffmpeg must be
/// UUID-based names generated by the server.
///
/// Returns `Ok(())` on exit code 0, or an `Err` containing the trimmed
/// stderr output on any non-zero exit.
///
/// # Errors
/// Returns an error if ffmpeg cannot be spawned (binary missing, permission
/// denied) or if the process exits with a non-zero status code.
pub fn run_ffmpeg(args: &[&str]) -> Result<()> {
    let output = Command::new("ffmpeg")
        .args(args)
        .output()
        .context("failed to spawn ffmpeg — is it installed and on PATH?")?;

    if output.status.success() {
        Ok(())
    } else {
        Err(anyhow::anyhow!(
            "ffmpeg exited with {}: {}",
            output.status,
            String::from_utf8_lossy(&output.stderr).trim()
        ))
    }
}

/// Convert an image file to WebP using ffmpeg.
///
/// Uses quality 85 and strips all metadata (`-map_metadata -1`).
/// Input can be any format ffmpeg understands (JPEG, PNG, BMP, TIFF, …).
///
/// # Errors
/// Returns an error if ffmpeg exits non-zero or cannot be spawned.
pub fn ffmpeg_image_to_webp(input: &Path, output: &Path) -> Result<()> {
    let in_str = path_to_str(input)?;
    let out_str = path_to_str(output)?;

    run_ffmpeg(&[
        "-loglevel",
        "error",
        "-i",
        in_str,
        "-map_metadata",
        "-1",
        "-quality",
        "85",
        "-y",
        out_str,
    ])
    .with_context(|| format!("image→webp conversion failed for {in_str}"))
}

/// Convert a GIF animation to `WebM` (VP9 codec) using ffmpeg.
///
/// Uses constant-quality VP9 encoding (`-crf 30 -b:v 0`) which preserves
/// animation while producing browser-compatible `WebM`.  All metadata is
/// stripped.
///
/// # Errors
/// Returns an error if ffmpeg exits non-zero or cannot be spawned.
pub fn ffmpeg_gif_to_webm(input: &Path, output: &Path) -> Result<()> {
    let in_str = path_to_str(input)?;
    let out_str = path_to_str(output)?;

    run_ffmpeg(&[
        "-loglevel",
        "error",
        "-i",
        in_str,
        "-c:v",
        "libvpx-vp9",
        "-crf",
        "30",
        "-b:v",
        "0",
        "-map_metadata",
        "-1",
        "-y",
        out_str,
    ])
    .with_context(|| format!("gif→webm conversion failed for {in_str}"))
}

/// Generate a WebP thumbnail from an image or video by extracting the first
/// frame and scaling to fit within `max_dim × max_dim`.
///
/// The `-2` height modifier ensures the scaled height is rounded to an even
/// number, required by many video codecs.  Aspect ratio is always preserved.
/// Thumbnail quality is fixed at 80 per project spec.
///
/// Works for both static images and video/animation sources.
///
/// # Errors
/// Returns an error if ffmpeg exits non-zero or cannot be spawned.
pub fn ffmpeg_thumbnail(input: &Path, output: &Path, max_dim: u32) -> Result<()> {
    let in_str = path_to_str(input)?;
    let out_str = path_to_str(output)?;
    let scale = format!("scale='if(gt(iw,ih),{max_dim},-2)':'if(gt(iw,ih),-2,{max_dim})'");

    run_ffmpeg(&[
        "-loglevel",
        "error",
        "-i",
        in_str,
        "-vframes",
        "1",
        "-vf",
        &scale,
        "-quality",
        "80",
        "-y",
        out_str,
    ])
    .with_context(|| format!("thumbnail generation failed for {in_str}"))
}

/// Probe the video codec of the first video stream in a file.
///
/// Returns the codec name in lower-case (e.g. `"av1"`, `"vp9"`, `"vp8"`).
/// Called by the `VideoTranscode` background worker to decide whether a `WebM`
/// upload needs AV1 → VP9 re-encoding.
///
/// Security: arguments are passed as a separate array — no shell invocation.
/// `file_path` must be a server-controlled UUID-based path.
///
/// # Errors
/// Returns an error if ffprobe cannot be run or returns no codec.
pub fn probe_video_codec(file_path: &str) -> Result<String> {
    let out = Command::new("ffprobe")
        .args([
            "-v",
            "error",
            "-select_streams",
            "v:0",
            "-show_entries",
            "stream=codec_name",
            "-of",
            "csv=p=0",
            file_path,
        ])
        .output()
        .context("ffprobe not found or failed to spawn")?;

    if !out.status.success() {
        return Err(anyhow::anyhow!(
            "ffprobe exit {}: {}",
            out.status,
            String::from_utf8_lossy(&out.stderr).trim()
        ));
    }

    let codec = String::from_utf8_lossy(&out.stdout)
        .trim()
        .to_ascii_lowercase();

    if codec.is_empty() {
        return Err(anyhow::anyhow!(
            "ffprobe returned no codec for: {file_path}"
        ));
    }

    Ok(codec)
}

/// Transcode a video file to `WebM` (VP9/CRF-33 + Opus/96k).
///
/// `input` and `output` must be on the same filesystem so the caller can
/// atomically rename `output` into its final location after this returns.
///
/// Encoding settings:
///   VP9  — `-deadline good -cpu-used 4` for a good quality/speed balance.
///           CRF 33 with unconstrained average bitrate (`-b:v 0`) — pure CRF
///           mode is the correct libvpx-vp9 approach.
///           `-row-mt 1` enables row-based multithreading.
///           `-tile-columns 2` improves encode and decode parallelism.
///   Opus — `-b:a 96k` — transparent quality for speech and music.
///
/// # Errors
/// Returns an error if ffmpeg cannot be spawned or exits non-zero.
pub fn ffmpeg_transcode_to_webm(input: &Path, output: &Path) -> Result<()> {
    let in_str = path_to_str(input)?;
    let out_str = path_to_str(output)?;

    run_ffmpeg(&[
        "-loglevel",
        "error",
        "-i",
        in_str,
        "-c:v",
        "libvpx-vp9",
        "-crf",
        "33",
        "-b:v",
        "0",
        "-deadline",
        "good",
        "-cpu-used",
        "4",
        "-row-mt",
        "1",
        "-tile-columns",
        "2",
        "-threads",
        "0",
        "-c:a",
        "libopus",
        "-b:a",
        "96k",
        "-y",
        out_str,
    ])
    .with_context(|| format!("video transcode failed for {in_str}"))
}

/// Generate a waveform PNG from an audio file using ffmpeg's `showwavespic` filter.
///
/// `input` is a path to the audio file on disk (UUID-named, server-controlled).
/// `output` receives the PNG.  Caller is responsible for atomically renaming
/// `output` into its final location.
///
/// # Errors
/// Returns an error if ffmpeg cannot be spawned or exits non-zero.
pub fn ffmpeg_audio_waveform(input: &Path, output: &Path, width: u32, height: u32) -> Result<()> {
    let in_str = path_to_str(input)?;
    let out_str = path_to_str(output)?;
    let vf = format!("showwavespic=s={width}x{height}:colors=#00c840|#007020:split_channels=0");

    run_ffmpeg(&[
        "-loglevel",
        "error",
        "-i",
        in_str,
        "-lavfi",
        &vf,
        "-frames:v",
        "1",
        "-y",
        out_str,
    ])
    .with_context(|| format!("audio waveform generation failed for {in_str}"))
}

// ─── Internal helpers ──────────────────────────────────────────────────────────

/// Convert a `Path` to a UTF-8 `&str`, returning a descriptive error if the
/// path contains non-UTF-8 bytes (rare on all supported platforms).
fn path_to_str(p: &Path) -> Result<&str> {
    p.to_str()
        .ok_or_else(|| anyhow::anyhow!("path contains non-UTF-8 characters: {}", p.display()))
}
